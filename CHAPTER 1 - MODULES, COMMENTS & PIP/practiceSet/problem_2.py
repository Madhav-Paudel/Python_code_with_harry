# use REPL and print the table of 5 using it 
# here REPL: 
# Read, Evaluate, Print, and Loop
# Python. REPL is an acronym for Read, Evaluate, Print, and Loop.
# Developers use REPL Python to communicate with the Python Interpreter.
# In contrast to running a Python file, you can input commands in the REPL 
# and see the results displayed immediately.

for x in range(1,11):
    print(f"5 x {x}:{5*x}")
print(" ")